package Lab02;
public class LoopDemo {
    public static void main(String[] args) {
        System.out.println("For Loop Example:");
        for (int i = 1; i <= 5; i++)
            System.out.println("Count: " + i);

        System.out.println("\nWhile Loop Example:");
        int j = 1;
        while (j <= 5) {
            System.out.println("Value: " + j);
            j++;
        }
    }
}
